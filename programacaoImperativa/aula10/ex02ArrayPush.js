// Push - método que adiciona novos itens ao final de uma matriz
let cores = ["Azul", "Branco", "Vermelho", "Verde"];
console.log("Array antes do Push:");
console.log(cores);
console.log(cores.length);

cores.push("Violeta", "Amarelo");

console.log("====================");
console.log("Array depois do Push:")
console.log(cores);
console.log(cores.length);