// Pop - método que elimina o último elemento
let cores = ["Azul", "Branco", "Vermelho", "Verde", "Violeta", "Amarelo"];
console.log("Array antes do Pop:");
console.log(cores);
console.log(cores.length);
console.log("====================");

let ultimoElemento = cores.pop();
console.log(ultimoElemento);

console.log("====================");
console.log("Array depois do Pop:")
console.log(cores);
console.log(cores.length);