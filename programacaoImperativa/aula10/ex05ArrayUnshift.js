// Unshift - método que adiciona novos itens ao início de uma matriz
let cores = ["Azul", "Branco", "Vermelho", "Verde"];
console.log("Array antes do Unshift:");
console.log(cores);
console.log(cores.length);

cores.unshift("Violeta", "Amarelo");

console.log("====================");
console.log("Array depois do Unshift:")
console.log(cores);
console.log(cores.length);